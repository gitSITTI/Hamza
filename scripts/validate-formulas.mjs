import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { validateModelFormulas } from "../local-networthgui/scaffold/model-formula-validation.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");
const appDir = path.join(rootDir, "local-networthgui");
const dataDir = path.join(appDir, "data");
const outputPath = path.join(dataDir, "formula-validation.json");
const markdownPath = path.join(appDir, "FORMULA_VALIDATION.md");
const checkOnly = process.argv.includes("--check");

const projectionRows = parseCsv(await readText(path.join(dataDir, "snapshots", "projection-audit.csv")));
const profile = await readJson(path.join(dataDir, "snapshots", "profile.json"));
const publicBenchmarks = await readJson(path.join(dataDir, "public-benchmarks.json"));
const govCache = {
  "bls-cpi-u": await readJson(path.join(dataDir, "gov-cache", "normalized", "bls-cpi-u.json")),
  "treasury-yield-curve": await readJson(path.join(dataDir, "gov-cache", "normalized", "treasury-yield-curve.json")),
  "ssa-life-table": await readJson(path.join(dataDir, "gov-cache", "normalized", "ssa-life-table.json")),
  "fhfa-hpi": await readJson(path.join(dataDir, "gov-cache", "normalized", "fhfa-hpi.json")),
};

const report = validateModelFormulas({
  projectionRows,
  publicBenchmarks,
  govCache,
  profileConfig: profile.config || {},
});

if (!checkOnly) {
  await writeFile(outputPath, `${JSON.stringify(report, null, 2)}\n`, "utf8");
  await writeFile(markdownPath, renderMarkdown(report), "utf8");
}

for (const check of report.checks) {
  const label = check.status.toUpperCase().padEnd(6);
  console.log(`${label} ${check.id}: ${check.note}`);
}
console.log(`Formula validation status: ${report.status} (${report.summary.pass} pass, ${report.summary.review} review, ${report.summary.fail} fail)`);

if (report.summary.fail > 0) {
  process.exitCode = 1;
}

async function readText(filePath) {
  return readFile(filePath, "utf8");
}

async function readJson(filePath) {
  return JSON.parse(await readText(filePath));
}

function parseCsv(text) {
  const rows = [];
  let cell = "";
  let row = [];
  let inQuotes = false;
  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];
    if (char === "\"" && inQuotes && next === "\"") {
      cell += "\"";
      index += 1;
    } else if (char === "\"") {
      inQuotes = !inQuotes;
    } else if (char === "," && !inQuotes) {
      row.push(cell);
      cell = "";
    } else if ((char === "\n" || char === "\r") && !inQuotes) {
      if (char === "\r" && next === "\n") index += 1;
      row.push(cell);
      if (row.some((value) => value !== "")) rows.push(row);
      row = [];
      cell = "";
    } else {
      cell += char;
    }
  }
  if (cell || row.length) {
    row.push(cell);
    rows.push(row);
  }
  const [header = [], ...body] = rows;
  return body.map((values) => Object.fromEntries(header.map((key, index) => [key, coerceNumber(values[index] ?? "")])));
}

function coerceNumber(value) {
  const trimmed = String(value).trim();
  if (!trimmed) return "";
  const normalized = trimmed.replace(/[$,%]/g, "");
  if (/^-?\d+(\.\d+)?$/.test(normalized)) return Number(normalized);
  return trimmed;
}

function renderMarkdown(report) {
  const rows = report.checks.map((check) => `| ${check.id} | ${check.status} | ${check.formula.replaceAll("|", "\\|")} | ${check.note.replaceAll("|", "\\|")} |`).join("\n");
  const sources = report.sources.map((source) => `- ${source.label}: ${source.url}`).join("\n");
  const findings = report.findings.length
    ? report.findings.map((finding) => `- ${finding.severity}: ${finding.id} - ${finding.finding}`).join("\n")
    : "- No review/fail findings.";
  return `# Formula Validation

Generated by \`scripts/validate-formulas.mjs\`.

Status: ${report.status}

Summary: ${report.summary.pass} pass, ${report.summary.review} review, ${report.summary.fail} fail.

## Checks

| ID | Status | Formula | Notes |
| --- | --- | --- | --- |
${rows}

## Findings

${findings}

## Official Sources

${sources}
`;
}
