export function listGovDataSources() {
  return Object.values(GOV_DATA_SOURCES);
}

export function getGovDataSource(id) {
  return listGovDataSources().find((source) => source.id === id) || null;
}

export function listGovDataSourcesByCategory(category) {
  return listGovDataSources().filter((source) => source.category === category);
}

export function listGovDataSourcesByGeography(geography) {
  return listGovDataSources().filter((source) => source.geography?.includes(geography));
}
